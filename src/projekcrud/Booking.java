    private void tampil_tabel_booking(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("Nama Paket");
        tb.addColumn("Deskripsi");
        tb.addColumn("Harga");
        
        try {
            String query = ("SELECT nama_paket,deskripsi,harga FROM data_barang");
            
            Connection conn = testkoneksi.configDB();
            
            Statement s = conn.createStatement();
            
            ResultSet result = s.executeQuery(query);
            
            while(result.next()){
                tb.addRow(new Object[]{
                    result.getString(1),result.getString(2),result.getString(3)
                });
            }
            tabel_barang_booking.setModel(tb);
            
        } catch (Exception e) {
        }
    }

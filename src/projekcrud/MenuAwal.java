    private void table_barangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_barangMouseClicked
        // TODO add your handling code here:
        int baris = table_barang.rowAtPoint(evt.getPoint());
        String id, namaMenu, deskripsi, harga, Stok;
        
        id = table_barang.getValueAt(baris, 0).toString();
        tid.setText(id);
        namaMenu = table_barang.getValueAt(baris, 1).toString();
        tnama1.setText(namaMenu);
        deskripsi = table_barang.getValueAt(baris, 2).toString();
        tnama2.setText(deskripsi);
        harga = table_barang.getValueAt(baris, 3).toString();
        tnama3.setText(harga);
        Stok = table_barang.getValueAt(baris, 4).toString();
        tnama4.setText(Stok);
    }//GEN-LAST:event_table_barangMouseClicked


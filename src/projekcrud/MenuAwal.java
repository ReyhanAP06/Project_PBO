    private void tampil_tabel(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("ID Barang");
        tb.addColumn("Nama Paket");
        tb.addColumn("Deskripsi");
        tb.addColumn("Harga");
        tb.addColumn("Stok");
        
        try {
            String query = ("SELECT * FROM data_barang");
            
            Connection conn = testkoneksi.configDB();
            
            Statement s = conn.createStatement();
            
            ResultSet result = s.executeQuery(query);
            
            while(result.next()){
                tb.addRow(new Object[]{
                    result.getString(1),result.getString(2),result.getString(3),result.getString(4),result.getString(5)
                });
            }
            table_barang.setModel(tb);
            
        } catch (Exception e) {
        }
    }
    
